package practice;

public class ASKII {
    public static void main(String[] args) {
        // printing letters to numbers

        char upperCase = 'A';
        char lowerCase = 'a';

        // char -> int
        int num = upperCase;
        int num2 = lowerCase;
        System.out.println(num); // 65
        System.out.println(num2); // 97
    }
}

