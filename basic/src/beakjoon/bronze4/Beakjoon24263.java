package beakjoon.bronze4;

import java.util.Scanner;

public class Beakjoon24263 {
    // 백준 24263. 알고리즘 수업 - 알고리즘의 수행 시간2
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        System.out.println(n);
        System.out.println(n=1);
    }
    // 문제의 출력은 O(1)일 경우 0, O(n)일 경우 1, O(n^2)일 경우 2, O(n^3)일 경우 3, O(n^3)보다 큰 시간복잡도가 필요한 경우 4를 출력
    //수행은 입력받은 횟수만큼, 시간 복잡도는 상수 시간이 소요되기 때문에 O(n) 문제에서는 1입니다.
}
